#Boolean als Ergebnis -> True oder False
print("Hallo" == "Hallo")# True
print(3 == 4) # False
print(3 != 4) # True
print(7 > 10) # False
print(7 < 10) # True

print(7 > 7) #False
print(7 >= 7) #True

print(10 < 10) #False
print(10 <= 10) #True
